window.addEventListener("DOMContentLoaded", () => {
  if (window.lucide) {
    window.lucide.createIcons();
  }

  const originals = new Map();
  document.querySelectorAll("[data-i18n], [data-i18n-html]").forEach((node) => {
    const key = node.dataset.i18n || node.dataset.i18nHtml;
    originals.set(key, node.dataset.i18nHtml ? node.innerHTML : node.textContent);
  });

  const toggle = document.querySelector("[data-lang-toggle]");
  const getStoredLanguage = () => {
    try {
      return localStorage.getItem("siteLanguage") || "zh";
    } catch {
      return "zh";
    }
  };

  const setStoredLanguage = (lang) => {
    try {
      localStorage.setItem("siteLanguage", lang);
    } catch {
      /* Language switching still works for the current page without storage. */
    }
  };

  const preferred = getStoredLanguage();

  const applyLanguage = (lang) => {
    document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
    document.querySelectorAll("[data-i18n]").forEach((node) => {
      const key = node.dataset.i18n;
      node.textContent = lang === "zh" && zhTranslations[key] ? zhTranslations[key] : originals.get(key);
    });
    document.querySelectorAll("[data-i18n-html]").forEach((node) => {
      const key = node.dataset.i18nHtml;
      node.innerHTML = lang === "zh" && zhTranslations[key] ? zhTranslations[key] : originals.get(key);
    });
    document.querySelectorAll("[data-lang-option]").forEach((node) => {
      node.classList.toggle("active", node.dataset.langOption === lang);
    });
    if (toggle) {
      toggle.setAttribute("aria-label", lang === "zh" ? "切换到英文" : "Switch to Chinese");
    }
    setStoredLanguage(lang);
  };

  if (toggle) {
    toggle.addEventListener("click", () => {
      const current = getStoredLanguage();
      applyLanguage(current === "zh" ? "en" : "zh");
    });
  }

  applyLanguage(preferred);

  document.querySelectorAll("[data-snapshot-carousel]").forEach((carousel) => {
    const cards = Array.from(carousel.querySelectorAll("[data-snapshot-card]"));
    const prev = carousel.querySelector("[data-carousel-prev]");
    const next = carousel.querySelector("[data-carousel-next]");
    const dotsWrap = carousel.querySelector("[data-carousel-dots]");
    let active = 0;

    const dots = cards.map((_, index) => {
      const dot = document.createElement("button");
      dot.type = "button";
      dot.className = "carousel-dot";
      dot.setAttribute("aria-label", `Show snapshot ${index + 1}`);
      dot.addEventListener("click", () => {
        active = index;
        render();
      });
      dotsWrap.appendChild(dot);
      return dot;
    });

    const render = () => {
      cards.forEach((card, index) => {
        const offset = (index - active + cards.length) % cards.length;
        let state = "far";
        if (offset === 0) state = "active";
        if (offset === 1) state = "next";
        if (offset === cards.length - 1) state = "prev";
        card.dataset.cardState = state;
        card.setAttribute("aria-hidden", state === "far" ? "true" : "false");
      });
      dots.forEach((dot, index) => {
        dot.classList.toggle("active", index === active);
      });
    };

    prev.addEventListener("click", () => {
      active = (active - 1 + cards.length) % cards.length;
      render();
    });

    next.addEventListener("click", () => {
      active = (active + 1) % cards.length;
      render();
    });

    render();
  });

  // ========== 内联编辑功能 ==========
  const EDIT_STORAGE_KEY = "inlineEdits";

  // 获取保存的编辑内容
  const getSavedEdits = () => {
    try {
      return JSON.parse(localStorage.getItem(EDIT_STORAGE_KEY) || "{}");
    } catch {
      return {};
    }
  };

  // 保存编辑内容
  const saveEdit = (key, value) => {
    const edits = getSavedEdits();
    edits[key] = value;
    try {
      localStorage.setItem(EDIT_STORAGE_KEY, JSON.stringify(edits));
    } catch {}
  };

  // 重置所有编辑
  const resetAllEdits = () => {
    try {
      localStorage.removeItem(EDIT_STORAGE_KEY);
    } catch {}
  };

  // 应用保存的编辑内容
  const applySavedEdits = () => {
    const edits = getSavedEdits();
    Object.keys(edits).forEach((key) => {
      const node = document.querySelector(`[data-i18n="${key}"], [data-i18n-html="${key}"]`);
      if (node) {
        if (node.dataset.i18nHtml !== undefined) {
          node.innerHTML = edits[key];
        } else {
          node.textContent = edits[key];
        }
      }
    });
  };

  // 创建编辑模式切换按钮
  const createEditToggle = () => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "edit-mode-toggle";
    btn.innerHTML = '<span class="edit-icon">✎</span><span class="edit-label">编辑</span>';
    btn.setAttribute("aria-label", "切换编辑模式");
    document.body.appendChild(btn);
    return btn;
  };

  // 创建重置按钮
  const createResetBtn = () => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "edit-reset-btn";
    btn.textContent = "重置内容";
    btn.style.display = "none";
    document.body.appendChild(btn);
    return btn;
  };

  const editToggle = createEditToggle();
  const resetBtn = createResetBtn();
  let editMode = false;

  // 切换编辑模式
  const toggleEditMode = () => {
    editMode = !editMode;
    document.body.classList.toggle("edit-mode-active", editMode);
    editToggle.classList.toggle("active", editMode);
    editToggle.querySelector(".edit-label").textContent = editMode ? "完成" : "编辑";
    resetBtn.style.display = editMode ? "block" : "none";

    const editableNodes = document.querySelectorAll("[data-i18n], [data-i18n-html]");
    editableNodes.forEach((node) => {
      if (editMode) {
        node.setAttribute("contenteditable", "true");
        node.classList.add("editable-field");
      } else {
        node.removeAttribute("contenteditable");
        node.classList.remove("editable-field");
      }
    });
  };

  // 监听编辑失焦，自动保存
  document.addEventListener("blur", (e) => {
    if (!editMode) return;
    const target = e.target;
    if (target.matches("[data-i18n], [data-i18n-html]")) {
      const key = target.dataset.i18n || target.dataset.i18nHtml;
      const value = target.dataset.i18nHtml !== undefined ? target.innerHTML : target.textContent;
      saveEdit(key, value);
      target.classList.add("edit-saved");
      setTimeout(() => target.classList.remove("edit-saved"), 800);
    }
  }, true);

  // 编辑模式下回车不换行（失焦保存）
  document.addEventListener("keydown", (e) => {
    if (!editMode) return;
    if (e.key === "Enter" && !e.shiftKey) {
      const target = e.target;
      if (target.matches("[data-i18n], [data-i18n-html]")) {
        e.preventDefault();
        target.blur();
      }
    }
    if (e.key === "Escape") {
      toggleEditMode();
    }
  });

  editToggle.addEventListener("click", toggleEditMode);

  resetBtn.addEventListener("click", () => {
    if (confirm("确定要重置所有文字为原始内容吗？此操作不可撤销。")) {
      resetAllEdits();
      // 重新应用语言（恢复原始内容）
      const currentLang = getStoredLanguage();
      applyLanguage(currentLang);
    }
  });

  // 初始化：应用保存的编辑
  applySavedEdits();

  // ========== 自由拖拽布局功能 ==========
  const LAYOUT_STORAGE_KEY = "layoutOffsets";
  const DRAGGABLE_SELECTORS = [
    ".metric-card",
    ".skill-card",
    ".detail-item",
    ".highlight-item",
    ".timeline-item",
    ".section-heading",
    ".contact-band",
    ".hero-lines",
    ".action-row"
  ];

  let layoutMode = false;
  let dragTarget = null;
  let dragStartX = 0;
  let dragStartY = 0;
  let startOffsetX = 0;
  let startOffsetY = 0;

  const getSavedLayout = () => {
    try {
      return JSON.parse(localStorage.getItem(LAYOUT_STORAGE_KEY) || "{}");
    } catch {
      return {};
    }
  };

  const saveLayout = (id, x, y) => {
    const layout = getSavedLayout();
    layout[id] = { x, y };
    try {
      localStorage.setItem(LAYOUT_STORAGE_KEY, JSON.stringify(layout));
    } catch {}
  };

  const resetAllLayout = () => {
    try {
      localStorage.removeItem(LAYOUT_STORAGE_KEY);
    } catch {}
    document.querySelectorAll(".draggable-item").forEach((el) => {
      el.style.transform = "";
    });
  };

  const initDraggableItems = () => {
    let index = 0;
    DRAGGABLE_SELECTORS.forEach((selector) => {
      document.querySelectorAll(selector).forEach((el) => {
        if (el.closest(".snapshot-carousel")) return;
        const id = `drag-${index++}`;
        el.dataset.dragId = id;
        el.classList.add("draggable-item");
      });
    });
  };

  const applySavedLayout = () => {
    const layout = getSavedLayout();
    Object.keys(layout).forEach((id) => {
      const el = document.querySelector(`[data-drag-id="${id}"]`);
      if (el && layout[id]) {
        el.style.transform = `translate(${layout[id].x}px, ${layout[id].y}px)`;
      }
    });
  };

  const createLayoutToggle = () => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "layout-mode-toggle";
    btn.innerHTML = '<span class="layout-icon">⤧</span><span class="layout-label">布局</span>';
    btn.setAttribute("aria-label", "切换布局调整模式");
    document.body.appendChild(btn);
    return btn;
  };

  const createLayoutResetBtn = () => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "layout-reset-btn";
    btn.textContent = "重置布局";
    btn.style.display = "none";
    document.body.appendChild(btn);
    return btn;
  };

  const layoutToggle = createLayoutToggle();
  const layoutResetBtn = createLayoutResetBtn();

  const toggleLayoutMode = () => {
    layoutMode = !layoutMode;
    document.body.classList.toggle("layout-mode-active", layoutMode);
    layoutToggle.classList.toggle("active", layoutMode);
    layoutToggle.querySelector(".layout-label").textContent = layoutMode ? "完成" : "布局";
    layoutResetBtn.style.display = layoutMode ? "block" : "none";
    document.querySelectorAll(".draggable-item").forEach((el) => {
      el.classList.toggle("layout-draggable", layoutMode);
    });
  };

  const getTranslate = (el) => {
    const transform = getComputedStyle(el).transform;
    if (!transform || transform === "none") return { x: 0, y: 0 };
    const match = transform.match(/matrix.*\((.+)\)/);
    if (match) {
      const values = match[1].split(", ");
      return { x: parseFloat(values[4]), y: parseFloat(values[5]) };
    }
    return { x: 0, y: 0 };
  };

  document.addEventListener("mousedown", (e) => {
    if (!layoutMode) return;
    const target = e.target.closest(".draggable-item");
    if (!target) return;
    dragTarget = target;
    dragStartX = e.clientX;
    dragStartY = e.clientY;
    const offset = getTranslate(target);
    startOffsetX = offset.x;
    startOffsetY = offset.y;
    target.classList.add("dragging");
    e.preventDefault();
  });

  document.addEventListener("mousemove", (e) => {
    if (!layoutMode || !dragTarget) return;
    const dx = e.clientX - dragStartX;
    const dy = e.clientY - dragStartY;
    dragTarget.style.transform = `translate(${startOffsetX + dx}px, ${startOffsetY + dy}px)`;
  });

  document.addEventListener("mouseup", () => {
    if (!layoutMode || !dragTarget) return;
    const id = dragTarget.dataset.dragId;
    const offset = getTranslate(dragTarget);
    saveLayout(id, offset.x, offset.y);
    dragTarget.classList.remove("dragging");
    dragTarget = null;
  });

  document.addEventListener("touchstart", (e) => {
    if (!layoutMode) return;
    const target = e.target.closest(".draggable-item");
    if (!target) return;
    const touch = e.touches[0];
    dragTarget = target;
    dragStartX = touch.clientX;
    dragStartY = touch.clientY;
    const offset = getTranslate(target);
    startOffsetX = offset.x;
    startOffsetY = offset.y;
    target.classList.add("dragging");
  }, { passive: true });

  document.addEventListener("touchmove", (e) => {
    if (!layoutMode || !dragTarget) return;
    const touch = e.touches[0];
    const dx = touch.clientX - dragStartX;
    const dy = touch.clientY - dragStartY;
    dragTarget.style.transform = `translate(${startOffsetX + dx}px, ${startOffsetY + dy}px)`;
  }, { passive: true });

  document.addEventListener("touchend", () => {
    if (!layoutMode || !dragTarget) return;
    const id = dragTarget.dataset.dragId;
    const offset = getTranslate(dragTarget);
    saveLayout(id, offset.x, offset.y);
    dragTarget.classList.remove("dragging");
    dragTarget = null;
  });

  layoutToggle.addEventListener("click", toggleLayoutMode);
  layoutResetBtn.addEventListener("click", () => {
    if (confirm("确定要重置所有元素位置吗？此操作不可撤销。")) {
      resetAllLayout();
    }
  });

  document.addEventListener("keydown", (e) => {
    if (!layoutMode) return;
    if (e.key === "Escape") {
      toggleLayoutMode();
    }
    if (dragTarget && (e.key === "ArrowUp" || e.key === "ArrowDown" || e.key === "ArrowLeft" || e.key === "ArrowRight")) {
      e.preventDefault();
      const offset = getTranslate(dragTarget);
      const step = e.shiftKey ? 10 : 1;
      let { x, y } = offset;
      if (e.key === "ArrowUp") y -= step;
      if (e.key === "ArrowDown") y += step;
      if (e.key === "ArrowLeft") x -= step;
      if (e.key === "ArrowRight") x += step;
      dragTarget.style.transform = `translate(${x}px, ${y}px)`;
      saveLayout(dragTarget.dataset.dragId, x, y);
    }
  });

  initDraggableItems();
  applySavedLayout();
});
